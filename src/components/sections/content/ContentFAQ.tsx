'use client'

import { useState } from 'react'
import { motion, AnimatePresence } from 'framer-motion'
import { ChevronDown } from 'lucide-react'
import { Section, FadeIn } from "../layout-helpers";
import { cn } from "@/lib/utils";

const faqs = [
    {
        question: "How does it source information for case studies?",
        answer: "Oden pulls from your CRM data (win notes, deal size, timeline), customer interviews (transcripts from Gong/Chorus), and any supporting documents you provide. It verifies facts before including them."
    },
    {
        question: "Can it match our brand voice?",
        answer: "Yes. Upload your style guide, provide example content, or let it analyze your existing materials. The agent learns your tone, preferred terminology, and writing style to ensure consistency."
    },
    {
        question: "What about approval workflows?",
        answer: "Content can route through your standard approval process. Set up stakeholders who need to sign off, and Oden will track status and send reminders until the piece is approved."
    }
];

export function ContentFAQ() {
    const [openIndex, setOpenIndex] = useState<number | null>(null)

    const toggleFAQ = (index: number) => {
        setOpenIndex(openIndex === index ? null : index)
    }

    return (
        <Section className="py-24" id="faq">
            <FadeIn>
                <h2 className="text-2xl md:text-3xl text-center mb-16 text-zinc-900">Common Questions</h2>
                <div className="max-w-4xl mx-auto">
                    {faqs.map((faq, index) => (
                        <div
                            key={index}
                            className="border-b border-zinc-100"
                        >
                            <button
                                onClick={() => toggleFAQ(index)}
                                className="flex w-full items-center justify-between text-left py-6 group"
                            >
                                <span className="text-base md:text-lg font-medium text-zinc-900 leading-tight">
                                    {faq.question}
                                </span>
                                <span className="flex items-center flex-shrink-0 ml-4">
                                    <ChevronDown
                                        className={cn(
                                            "h-4 w-4 text-zinc-400 transition-transform duration-300",
                                            openIndex === index && "rotate-180"
                                        )}
                                    />
                                </span>
                            </button>

                            <AnimatePresence initial={false}>
                                {openIndex === index && (
                                    <motion.div
                                        initial={{ height: 0, opacity: 0 }}
                                        animate={{ height: "auto", opacity: 1 }}
                                        exit={{ height: 0, opacity: 0 }}
                                        transition={{ duration: 0.3, ease: [0.04, 0.62, 0.23, 0.98] }}
                                        className="overflow-hidden"
                                    >
                                        <p className="pb-6 text-base md:text-base leading-relaxed text-zinc-500 max-w-4xl">
                                            {faq.answer}
                                        </p>
                                    </motion.div>
                                )}
                            </AnimatePresence>
                        </div>
                    ))}
                </div>
            </FadeIn>
        </Section>
    )
}
